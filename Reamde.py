# هذا البرنامج يوضح كيفية إنشاء دالة في بايثون لجمع رقمين

# تعريف دالة اسمها add
# الدالة تستقبل رقمين: a و b
# وتعيد ناتج جمعهما
def add(a, b):
    return a + b


# أخذ رقم من المستخدم وتخزينه في المتغير x
# input تطلب إدخال قيمة من المستخدم
# int تحول الإدخال من نص إلى عدد صحيح
x = int(input("ادخل الرقم الأول: "))

# أخذ رقم ثاني من المستخدم وتخزينه في المتغير y
y = int(input("ادخل الرقم الثاني: "))

# طباعة نتيجة جمع الرقمين باستخدام الدالة add
print("ناتج الجمع =", add(x, y))


# إعادة تعريف الدالة add (نفس التعريف السابق)
def add(a, b):
    return a + b


# اختبار الدالة بقيم ثابتة
print(add(2, 3))     # الناتج: 5
print(add(5, 7))     # الناتج: 12
print(add(-2, 4))    # الناتج: 2